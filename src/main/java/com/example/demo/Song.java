package com.example.demo;

public class Song {
    private long id;
    private String name;
    private String artist;
    private String album;
    private int rating;
    private int year;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getArtist() {
        return artist;
    }

    public String getAlbum() {
        return album;
    }

    public int getRating() {
        return rating;
    }

    public int getYear() {
        return year;
    }
}
